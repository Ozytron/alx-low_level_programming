#ifndef FUNCTION_LIKE_MICRO_H
#define FUNCTION_LIKE_MICRO_H

/**
 * File: 3-function_like_macro.h
 * Desc: a function-like macro ABS(x) that
 *       computes the absolute value of a number x.
 * @x: parameter.
 */

#define ABS(x) ((x) < 0 ? ((x) * -1) : ((x) * 1))

#endif /* FUNCTION_LIKE_MICRO_H */
